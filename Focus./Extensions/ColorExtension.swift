import UIKit

extension UIColor {
    static var primary: UIColor {
        return UIColor(red: 0, green: 253/255, blue: 254/255, alpha: 1)
    }
    
    static var secondary: UIColor {
        return UIColor(red: 0, green: 250/255, blue: 146/255, alpha: 1)
    }
}
